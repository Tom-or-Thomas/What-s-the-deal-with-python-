# String
firstName =  "Thomas" # Strings can be double quotes or single quotes
lastName = 'Smith'

# Print fill strings
print(firstName, lastName)

# Print individual characters 
print(firstName[0], firstName[1], firstName[2], firstName[3], firstName[4], firstName[5])


# Cool thing about Python, can use negative index to get 
print(firstName[-5], firstName[-4], firstName[-3], firstName[-2], firstName[-1], firstName[0])

# Get a range of characters
print(firstName[0:6])